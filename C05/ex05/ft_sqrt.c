int	ft_sqrt(int nb)
{
	long	nc;
	long	nd;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	nc = nb;
	nd = 2;
	while (nd * nd <= nc)
	{
		if (nd * nd == nc)
			return (nd);
		nd++;
	}
	return (0);
}
