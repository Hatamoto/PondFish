int	ft_iterative_factorial(int nb)
{
	int	i;

	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (1);
	i = nb;
	while (i > 1)
	{
		i--;
		nb *= i;
	}
	return (nb);
}
